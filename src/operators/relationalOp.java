package operators;

public class relationalOp {
    public static void main(String[] args) {
        int a = 15, b = 20;
        System.out.println("a="+a+" b="+b);
        System.out.println("a == b: "+(a==b));
        System.out.println("a != b: "+ (a!=b));
        System.out.println("a > b: "+(a>b));
        System.out.println("a < b: "+(a<b));
        System.out.println("a >= b: "+(a>=b));
        System.out.println("a <= b: "+(a<=b));
    }
}
